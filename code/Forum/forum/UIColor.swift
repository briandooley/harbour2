import Foundation
import UIKit

extension UIColor{
    
    static func redHatDarkRed() -> UIColor{
        return UIColor(colorLiteralRed: 0.4902, green: 0, blue: 0, alpha: 1)
    }
    
    static func redHatLightRed() -> UIColor{
        return UIColor(colorLiteralRed: 0.5725, green: 0.0078, blue: 0.0510, alpha: 1)
    }

}